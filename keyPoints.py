def build_model(input_shape):
    inputs = tf.keras.Input(shape=input_shape)

    # Блок сверток с BatchNormalization и Dropout
    x = tf.keras.layers.Conv2D(32, (3, 3), activation='relu', padding='same')(inputs)
    x = tf.keras.layers.BatchNormalization()(x)
    x = tf.keras.layers.MaxPooling2D(pool_size=(2, 2))(x)
    x = tf.keras.layers.Dropout(0.3)(x)  # Регуляризация

    x = tf.keras.layers.Conv2D(64, (3, 3), activation='relu', padding='same')(x)
    x = tf.keras.layers.BatchNormalization()(x)
    x = tf.keras.layers.MaxPooling2D(pool_size=(2, 2))(x)
    x = tf.keras.layers.Dropout(0.3)(x)

    x = tf.keras.layers.Conv2D(128, (3, 3), activation='relu', padding='same')(x)
    x = tf.keras.layers.BatchNormalization()(x)
    x = tf.keras.layers.MaxPooling2D(pool_size=(2, 2))(x)
    x = tf.keras.layers.Dropout(0.3)(x)

    x = tf.keras.layers.Conv2D(128, (3, 3), activation='relu', padding='same')(x)
    x = tf.keras.layers.BatchNormalization()(x)
    x = tf.keras.layers.MaxPooling2D(pool_size=(2, 2))(x)
    x = tf.keras.layers.Dropout(0.3)(x)

    # Развертывание в плоский вектор
    x = tf.keras.layers.Flatten()(x)

    # Полносвязный слой
    x = tf.keras.layers.Dense(256, activation='relu')(x)  # Увеличение нейронов в слое
    x = tf.keras.layers.Dropout(0.5)(x)  # Регуляризация

    # Финальный слой предсказания 4 координат (x, y, ширина, высота)
    output = tf.keras.layers.Dense(4, activation='linear')(x)

    # Создание модели
    model = tf.keras.Model(inputs=inputs, outputs=output)

    return model

