  contours, _ = cv2.findContours(dilated_edges, cv2.RETR_EXTERNAL, cv2.CHAIN_APPROX_SIMPLE)

  # Параметры для отсечения пропорций экрана
  min_aspect_ratio = 2/3
  max_aspect_ratio = 16/9

