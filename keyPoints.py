def data_generator(coco, file_path, cat_ids,  target_size, batch_size=8):
    img_ids = coco.getImgIds(catIds=cat_ids)
    imgs = coco.loadImgs(img_ids) #

    while True:
        images = []
        bboxes = []
        for img_info in imgs:
            try:
                # Загружаем изображение
                img_name = file_path + img_info['file_name']
                img = cv2.imread(img_name)
                if img is None:
                    print("Error loading image")
                    continue
                img_gray = cv2.cvtColor(img, cv2.COLOR_BGR2GRAY)

                # Извлекаем bounding box
                ann_ids = coco.getAnnIds(imgIds=img_info['id'], catIds=cat_ids, iscrowd=False)
                annotations = coco.loadAnns(ann_ids)

                if len(annotations) == 0:
                    continue

                ann = annotations[0]  # Берем первую аннотацию (если их несколько, можно модифицировать)
                x, y, w, h = ann['bbox']

                # Подгонка изображения под нужный размер
                img_gray = cv2.resize(img_gray, target_size)
                img_gray = np.expand_dims(img_gray, axis=-1)

                # Нормализуем координаты bbox относительно размеров изображения
                img_h, img_w = img_info['height'], img_info['width']
                bbox = [x / img_w, y / img_h, w / img_w, h / img_h]

                images.append(img_gray)
                bboxes.append(bbox)

                if len(images) == batch_size:
                    yield (np.array(images), np.array(bboxes))
                    images = []
                    bboxes = []

            except Exception as e:
                print(f"Error processing image {img_info['file_name']}: {e}")
                continue

        if len(images) > 0:
            yield (np.array(images), np.array(bboxes))
            images = []
            bboxes = []

